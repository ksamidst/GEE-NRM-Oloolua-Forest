// Define the Area of Interest from the Oloolua forest shapefile

//Use the Standard method for masking cloud pixels in the Sentinel-2 images
function maskS2clouds(image) {
  var qa = image.select('QA60');
  
  // Bits 10 and 11 are clouds and cirrus, respectively.
  var cloudBitMask = 1 << 10;
  var cirrusBitMask = 1 << 11;
  
 // Both flags should be set to zero, indicating clear conditions. 
   var mask = qa.bitwiseAnd(cloudBitMask).eq(0)
      .and(qa.bitwiseAnd(cirrusBitMask).eq(0));
 
  return image.updateMask(mask).divide(10000);
}

//Generate a cloud free Sentinel-2 image of the Oloolua forest area for 2020
var clipped = sentinel2
              //Filter the dataset to the 2020 full year date range
              .filterDate('2020-01-01','2020-12-31')
              //Maintain the image metadata for the area of interest
              .filterBounds(AOI)
              //Apply the cloud masking function to the remaining images in the filtered collection
              .map(maskS2clouds)
              //generate a single image from the filtered collection using the median() reduction function
              .median()
              //and clip it to the area of interest
              .clip(AOI);
            
//Center the map onto the area of interest to a zoom level of 12
Map.centerObject(AOI,12);

//Visualize the true color RGB generated image
Map.addLayer(clipped,{min: 0.0,max: 0.3,bands:['B4','B3','B2']},'Oloolua 2020');

